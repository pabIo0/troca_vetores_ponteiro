#include <stdio.h>
#include <stdlib.h>

void troca(int v1, int v2)
{
    int v3;
    v3 = v1;
    v1 = v2;
    v2 = v3;
}
int main()
{
    int v1, v2;
    printf("Digite 2 valores inteiros: ");
    scanf("%d%d", &v1, &v2);

    printf("\nv1: %d\tv2: %d\n", v1, v2);
    troca(&v1, &v2);
    printf("\nApos a troca:\n\nv1: %d\tv2: %d\n", v1, v2);

    return 0;
}
